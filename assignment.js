// Assignment Operators

let x = 10;
let y = 5;

// Addition assignment
x += y; 
console.log(x); 

// Subtraction assignment
x -= y; 
console.log(x); 

// Multiplication assignment
x *= y;
console.log(x);

// Division assignment
x /= y; 
console.log(x); 

// Modulus assignment
x %= y; 
console.log(x);

// Exponentiation assignment
x **= y; 
console.log(x); 
