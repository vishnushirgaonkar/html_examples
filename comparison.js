// Comparison operators in JavaScript

// Equal to (==)
console.log(5 == 5); 
console.log(5 == '5'); 

// Not equal to (!=)
console.log(5 != 3); 
console.log(5 != '5'); 

// Strict equal to (===)
console.log(5 === 5); 
console.log(5 === '5'); 

// Strict not equal to (!==)
console.log(5 !== 3);
console.log(5 !== '5');

// Greater than (>)
console.log(5 > 3); 
console.log(5 > 7); 

// Less than (<)
console.log(5 < 7); 
console.log(5 < 3); 

// Greater than or equal to (>=)
console.log(5 >= 3);
console.log(5 >= 5); 

// Less than or equal to (<=)
console.log(5 <= 7); 
console.log(5 <= 3); 
