// Logical AND (&&)
let x = 5;
let y = 10;

if (x > 0 && y > 0) {
  console.log("Both x and y are greater than 0");
}

// Logical OR (||)
let a = 15;
let b = 20;

if (a > 10 || b > 10) {
  console.log("Either a or b is greater than 10");
}

// Logical NOT (!)
let isTrue = true;

if (!isTrue) {
  console.log("isTrue is false");
}
